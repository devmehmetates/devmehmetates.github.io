function myFunction(color) {
    var element = document.getElementById("demo");
    if(color === 1){
        element.style.color = "red";
    }else if(color == 2){
        element.style.color = "blue";
    }else if(color == 3){
        element.style.color = "green";
    }else if(color == 4){
        element.style.color = "yellow";
    }else if(color == 5){
        element.style.color = "pink";
    }
    
   
};